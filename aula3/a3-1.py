# Procedimento: Não possui retorno
# Função: Existe retorno
# paramento: é a variavél recebida em uma função ou procedimento
# argumento: é o dado passado durante a chamada de uma função ou procedimento

def somar(numero1, numero2): # Parametro
    resultado = numero1 + numero2
    print(resultado)

somar(10, 12) # Argumento

def diminuir(numero1, numero2):
    resultado = numero1 - numero2
    print(resultado)

diminuir(10, 12)


def dividir(numero1, numero2):
    resultado = numero1 / numero2
    print(resultado)

dividir(10, 5)

def multiplicar(numero1, numero2):
    resultado = numero1 * numero2
    print(resultado)

multiplicar(10, 5)
