def soma(numero1, numero2):
    resultado = numero1 + numero2
    return resultado

r = soma(5, 8)
print(r)

def diminuir(numero1, numero2):
    resultado = numero1 - numero2
    return resultado

r = diminuir(5, 8)
print(r)

def dividir(numero1, numero2):
    resultado = numero1 / numero2
    return resultado

r = dividir(5, 1)
print(r)

def multiplicar(numero1, numero2):
    resultado = numero1 / numero2
    return resultado

r = multiplicar(5, 2)
print(r)