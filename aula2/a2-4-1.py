# criação de lista númerica
range(5) # cria um conjunto numerico 0 a 5


# atribui cada valor do conjunto a variavel numero
# ou seja, executa o codigo repetidamente por 5 vezes (de 0 a 4)
for numero in range(5):
    print(numero)

#comecando em 1 indo até 5
for numero in range(1,5):
    print(numero)

#comecando em 1 indo até 9 de 2 em 2
for numero in range(1,9,2):
    print(numero)